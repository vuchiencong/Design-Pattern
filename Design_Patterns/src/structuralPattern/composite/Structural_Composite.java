package structuralPattern.composite;

/**
 * Composite Pattern là một sự tổng hợp những thành phần có quan hệ với nhau để tạo ra thành phần lớn hơn.
 * Nó cho phép thực hiện các tương tác với tất cả đối tượng trong mẫu tương tự nhau.
 */
public class Structural_Composite {
    /**
     * Composite Pattern được sử dụng khi chúng ta cần xử lý một nhóm đối tượng tương tự theo cách xử lý 1 object.
     * Composite pattern sắp xếp các object theo cấu trúc cây để diễn giải 1 phần cũng như toàn bộ hệ thống phân cấp.
     * Pattern này tạo một lớp chứa nhóm đối tượng của riêng nó.
     * Lớp này cung cấp các cách để sửa đổi nhóm của cùng 1 object.
     * Pattern này cho phép Client có thể viết code giống nhau để tương tác với composite object này,
     * bất kể đó là một đối tượng riêng lẻ hay tập hợp các đối tượng.
     */

    /**
     * Composite Pattern chỉ nên được áp dụng khi nhóm đối tượng phải hoạt động
     * như một đối tượng duy nhất (theo cùng một cách).
     * Composite Pattern có thể được sử dụng để tạo ra một cấu trúc giống như cấu trúc cây.
     */
}
